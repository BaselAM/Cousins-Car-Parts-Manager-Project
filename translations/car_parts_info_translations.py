def translate_category(category, language):
    """
    Translate a category name

    Args:
        category: The category name in English
        language: The target language code

    Returns:
        Translated category name
    """
    if not category or category == "-":
        return "-"

    # Get the translation dictionary for this language
    translations = CATEGORY_TRANSLATIONS.get(language, {})

    # Return the translation or the original if not found
    return translations.get(category, category)


