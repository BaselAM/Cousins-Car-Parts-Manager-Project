def translate_compatible_models(models_str, language):
    """
    Translate compatible models string

    Args:
        models_str: String of compatible models in format "Brand:Model, Brand:Model"
        language: The target language code

    Returns:
        Translated compatible models string
    """
    if not models_str or models_str == "-":
        return "-"

    result_parts = []
    # Split by commas for multiple entries
    entries = models_str.split(',')

    for entry in entries:
        entry = entry.strip()
        if ':' in entry:
            # Format is typically "Brand:Model"
            parts = entry.split(':', 1)
            if len(parts) == 2:
                brand, model = parts
                brand = brand.strip()
                model = model.strip()

                # Translate the brand
                translated_brand = translate_brand(brand, language)

                # For the Generic Model term, we should translate it
                if model == "Generic Model":
                    model = translate_misc("Generic Model", language)

                result_parts.append(f"{translated_brand}:{model}")
            else:
                result_parts.append(entry)  # Just keep the original if the split didn't work
        else:
            # If no colon, just use the whole entry
            result_parts.append(entry)

    return ', '.join(result_parts)